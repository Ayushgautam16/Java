    // a functional interface has exactly one abstract method.

    // functional interfaces are used to define a contract between a class and its methods.

    // functional interfaces are useful when you want to use a lambda expression or method reference to implement a single abstract method.

// lambda interface 

    // a lambda interface is a functional interface that has exactly one abstract method.

    // lambda interfaces are used to create anonymous functions.

    // lambda interfaces can be used to define a contract between a class and its methods using lambda expressions or method references.

    // Example:

    // class interface1{
// public static void main(String[] args) {
    // interface MyFunctionalInterface {
        // void myMethod();
    // }
    
    // MyFunctionalInterface myLambda = () -> System.out.println("Hello World!");
    // myLambda.myMethod();
    // Output: Hello World!


    // MyFunctionalInterface myLambda2 = System.out::println;
    // myLambda2.myMethod();
    // Output: Hello World!
